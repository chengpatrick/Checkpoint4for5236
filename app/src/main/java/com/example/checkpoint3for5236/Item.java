package com.example.checkpoint3for5236;

public class Item {
    String topic;
    String sum;

    public Item(String topic, String sum) {
        this.topic = topic;
        this.sum = sum;
    }

    public String getTopic() {
        return topic;
    }

    public void setTopic(String topic) {
        this.topic = topic;
    }

    public String getSum() {
        return sum;
    }

    public void setSum(String sum) {
        this.sum = sum;
    }

}
